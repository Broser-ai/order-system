import { NextRequest, NextResponse } from "next/server";
import { createClient } from "@/lib/supabase/server";

export async function POST(request: NextRequest) {
  const supabase = await createClient();
  const { data: { user } } = await supabase.auth.getUser();
  if (!user) return NextResponse.json({ error: "Unauthorized" }, { status: 401 });

  const { name, description, tools } = await request.json();
  if (!name || !tools?.length) return NextResponse.json({ error: "name and tools required" }, { status: 400 });

  const toolDefs = tools.map((t: { name: string; description: string; params: string }) => {
    const params = t.params ? t.params.split(",").map((p: string) => p.trim()).filter(Boolean) : [];
    return `
@mcp.tool()
def ${t.name}(${params.join(", ")}) -> dict:
    """${t.description}"""
    # TODO: implement ${t.name}
    return {"status": "ok", "tool": "${t.name}"}`;
  }).join("\n");

  const code = `"""
${name} — FastMCP Server
${description}
Generated by Master Team Console
"""

from fastmcp import FastMCP

mcp = FastMCP("${name}")
${toolDefs}

if __name__ == "__main__":
    mcp.run()
`;

  const { data: server } = await supabase.from("custom_mcp_servers").insert({
    user_id: user.id,
    name,
    description: description ?? "",
    tools,
    generated_code: code,
    status: "generated",
  }).select().single();

  return NextResponse.json({ code, server });
}
